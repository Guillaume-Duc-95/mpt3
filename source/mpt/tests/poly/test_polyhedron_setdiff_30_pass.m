function test_polyhedron_setdiff_30_pass
% A = B \cup C, all lower-dimensional, reported by Nick Eckenstein

AHe = [-5.0616785370083 -3.34199369232998 -1 -5.0616785370083];
AH = [-1 -1.73205080756887 0 -1.01674682452694;1 1.25258182431356 0 1;-1.51456854889495 -1 0 -1.35789596845292];
BHe = [-5.06167853700814 -3.34199369232988 -1 -5.06167853700815];
BH = [0.329576114559585 0.65915222911917 -0.0821604327502697 0.329576114559585;1.57368383749022 1 0 1.40651923191757;-1.51456854889494 -1 0 -1.35789596845292];
CHe = [-5.06167853700829 -3.34199369232998 -1 -5.06167853700829];
CH = [0.329576114559585 0.65915222911917 -0.0821604327502697 0.329576114559585;-1 -1.73205080756887 0 -1.01674682452695;-1.57368383749022 -1 0 -1.40651923191757];
A = Polyhedron('H', AH, 'He', AHe);
B = Polyhedron('H', BH, 'He', BHe);
C = Polyhedron('H', CH, 'He', CHe);
assert(A\C==B);
assert(A\B==C);
assert(isEmptySet(A\[C B]));
assert(isEmptySet(A\[B C]));
assert(isEmptySet([B C]\A));
assert(isEmptySet(C\A));
assert(isEmptySet(B\A));

end

function test_polyhedron_setdiff_05_pass
%
% set difference between unbounded polyhedron and full-dimensional polyhedron in 3D
%

H1 = [1.3067      0.63255      0.18343     -0.37667
     -0.28438       1.9033     -0.25973     -0.69804
     -0.79171        -1.16      -1.3548      0.30412
    -0.094646      -1.6951     -0.76199     -0.44661 ];

P = Polyhedron('H',H1);

H2 = [0.36431      -1.0246     -0.18906     -0.25894
      -0.7637        0.505     0.086573       2.0853
      0.54952     0.040473       1.5126        9.923
     -0.31023       1.6766     -0.32585     -0.54969];


Q = Polyhedron('H',H2);

R = P\Q;

% result are 3 unbounded polyhedra
H{1} = [1.3067      0.63255      0.18343     -0.37667
     -0.28438       1.9033     -0.25973     -0.69804
    -0.094646      -1.6951     -0.76199     -0.44661
     -0.36431       1.0246      0.18906      0.25894]; 
H{2} = [1.3067      0.63255      0.18343     -0.37667
     -0.28438       1.9033     -0.25973     -0.69804
     -0.79171        -1.16      -1.3548      0.30412
    -0.094646      -1.6951     -0.76199     -0.44661
      0.36431      -1.0246     -0.18906     -0.25894
       0.7637       -0.505    -0.086573      -2.0853];   
H{3} = [1.3067      0.63255      0.18343     -0.37667
     -0.28438       1.9033     -0.25973     -0.69804
      0.36431      -1.0246     -0.18906     -0.25894
      -0.7637        0.505     0.086573       2.0853
     -0.54952    -0.040473      -1.5126       -9.923];
  
% since the result can be arbitrarily ordered, we need to test each combination 
ts = false(3);
for i=1:3
    for j=1:3
      ts(i,j) = Polyhedron('H',H{i}) == R(j);
    end
end

if any(sum(ts,2)>1)
    error('Here should be 3 non-overlapping regions.');
end

end